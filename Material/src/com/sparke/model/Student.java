package com.sparke.model;
/**
 * 学生
 * @author 运德
 *
 */
public class Student {
	/**
	 * 学号
	 */
	private String mNumber;
	/**
	 * 密码
	 */
	private String mPsw;
	/**
	 * 姓名
	 */
	private String mName;
	/**
	 * 性别
	 */
	private String mSex;
	/**
	 * 出生日期
	 */
	private String mBorn;
	/**
	 * 家乡
	 */
	private String mHome;
	/**
	 * 学院
	 */
	private String mCollege;
	/**
	 * 专业
	 */
	private String mMajor;
	/**
	 * 班级
	 */
	private String mClass;
	/**
	 * 入学年份
	 */
	private String mYear;
	/**
	 * 宿舍号
	 */
	private String mRoom;
	
	public String getNumber() {
		return mNumber;
	}
	public void setNumber(String mNumber) {
		this.mNumber = mNumber;
	}
	public String getPsw() {
		return mPsw;
	}
	public void setPsw(String mPsw) {
		this.mPsw = mPsw;
	}
	public String getName() {
		return mName;
	}
	public void setName(String mName) {
		this.mName = mName;
	}
	public String getSex() {
		return mSex;
	}
	public void setSex(String mSex) {
		this.mSex = mSex;
	}
	public String getBorn() {
		return mBorn;
	}
	public void setBorn(String mBorn) {
		this.mBorn = mBorn;
	}
	public String getHome() {
		return mHome;
	}
	public void setHome(String mHome) {
		this.mHome = mHome;
	}
	public String getCollege() {
		return mCollege;
	}
	public void setCollege(String mCollege) {
		this.mCollege = mCollege;
	}
	public String getMajor() {
		return mMajor;
	}
	public void setMajor(String mMajor) {
		this.mMajor = mMajor;
	}
	public String getmClass() {
		return mClass;
	}
	public void setClass(String mClass) {
		this.mClass = mClass;
	}
	public String getYear() {
		return mYear;
	}
	public void setYear(String mYear) {
		this.mYear = mYear;
	}
	public String getRoom() {
		return mRoom;
	}
	public void setRoom(String mRoom) {
		this.mRoom = mRoom;
	}	
}
